package exercises;

public class GetMetric {

    private double centimeters;
    private double liters;
    private double inches;
    private double gallons;



    public void setCentimeters( double cent){
        centimeters = cent;
    }
    public double getCentimeters(){
        return centimeters;
    }

    public void setLiters(double lit) {
        liters = lit;
    }

    public double getLiters() {
        return liters;
    }
    public void setInches( double inch){
        inches = inch;
    }
    public double getInches(){
        return inches;
    }
    public void setGallons( double gall){
        gallons = gall;
    }
    public double getGallons(){
        return gallons;
    }

}
